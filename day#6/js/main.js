/*

Iterate 0 to 10 using for loop, do the same using while and do while loop


*/

// Solution

for (let i = 0; i <= 10; i++) {
  console.log("For loop I", i);
}

let i1 = 0;
while (i1 <= 10) {
  console.log("While I ", i1);
  i1++;
}

let i2 = 0;
do {
  console.log("Do ... while I", i2);
  i2++;
} while (i2 <= 10);
